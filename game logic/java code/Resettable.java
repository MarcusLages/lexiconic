/**
 * Interface used to reset the concrete classes
 * that implements it.
 *
 * @author Marcus Vinicius Santos Lages
 * @version 1.0
 */
public interface Resettable {

    /**
     * Method used to reset the Object that implements it to
     * its starting stage.
     */
    void reset();

}
